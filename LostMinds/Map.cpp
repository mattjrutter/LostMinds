Map::Map(){
	floor = TextureManager::loadTexture("res/floor.png");
	wall = TextureManager::loadTexture("res/wall.png");

	loadMap(lvl1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}

Map::~Map() { 
	SDL_DestroyTexture(wall);
	SDL_DestroyTexture(floor);
}

void Map::loadMap(int arr[24][40]) {
	for (int row = 0; row < 24; row++) {
		for (int col = 0; col < 40; col++) {
			map[row][col] = arr[row][col];
		}
	}
}

void Map::drawMap() {
	int type = 0;

	for (int row = 0; row < 24; row++) {
		for (int col = 0; col < 40; col++) {
			type = map[row][col];
			dest.x = col * 32;
			dest.y = row * 32;
			switch (type)
			{
			case 0:
				TextureManager::draw(floor, src, dest);
				break;
			case 1:
				TextureManager::draw(wall, src, dest);
				break;
			default:
				break;
			}
		}
	}
}